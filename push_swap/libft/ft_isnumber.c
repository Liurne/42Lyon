#include "../push_swap.h"

int	ft_isnumber(char *s)
{
	int	i;

	i = 0;
	if (!s)
		return (0);
	if (s[i] == '-')
		i++;
	while (s[i])
	{
		if (s[i] < '0' || s[i] > '9')
			return (0);
		i++;
	}
	return (1);
}