#include "../pipex.h"

int	ft_putstr_fd(char *s, int fd)
{
	size_t	i;

	i = 0;
	if (!s)
		write(fd, "(null)", 6);
	while (s[i])
		i++;
	write(fd, s, i);
	return (1);
}
