int	ft_atoi(char *str)
{
	int			i;
	int			m;
	long int	res;

	res = 0;
	i = 0;
	m = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == ' ')
		i++;
	while (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			m++;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		res = (res * 10) + (str[i] - '0');
		i++;
	}
	if (m % 2 == 1)
		res = -res;
	return (res);
}
