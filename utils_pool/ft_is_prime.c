int	ft_is_prime(int nb)
{
	int	res;

	res = 2;
	if (nb < 2)
		return (0);
	while (res * res <= nb)
	{
		if (nb % res == 0 && res != nb)
			return (0);
		res++;
	}
	return (1);
}
