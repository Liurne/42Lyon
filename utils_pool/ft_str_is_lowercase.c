int	ft_str_is_lowercase(char *str)
{
	int	i;
	int	t;

	i = 0;
	t = 1;
	if (str[0] == '\0')
	{
		return (1);
	}
	while (str[i] != '\0')
	{
		if (str[i] < 'a' || str[i] > 'z')
		{
			t = 0;
		}
		i++;
	}
	return (t);
}
