int	ft_str_is_uppercase(char *str)
{
	int	i;
	int	t;

	i = 0;
	t = 1;
	if (str[0] == '\0')
	{
		return (1);
	}
	while (str[i] != '\0')
	{
		if (str[i] < 'A' || str[i] > 'Z')
		{
			t = 0;
		}
		i++;
	}
	return (t);
}
