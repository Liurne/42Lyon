#include "ft_printf.h"

int	ft_putnbr(int n)
{
	size_t	c;
	int		s;

	c = 0;
	s = 0;
	if (n == -2147483648)
		return (write(1, "-2147483648", 11));
	else
	{
		if (n < 0)
		{
			n *= -1;
			s += write(1, "-", 1);
		}
		if (n > 9)
			s += ft_putnbr(n / 10);
		c = '0' + n % 10;
		return (s += write(1, &c, 1));
	}
}
